function plus(){

    var x = document.getElementById("first_plus").value;
    var y = document.getElementById("second_plus").value;

    document.write(parseInt(x)+parseInt(y));
}

function minus(){

    var x = document.getElementById("first_minus").value;
    var y = document.getElementById("second_minus").value;

    document.write(parseInt(x)-parseInt(y));
}

function multiply(){

    var x = document.getElementById("first_multiply").value;
    var y = document.getElementById("second_multiply").value;

    document.write(parseInt(x)*parseInt(y));
}